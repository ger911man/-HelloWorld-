
public class mathCalc {
	public static void main(String args[]){
		System.out.println(Math.abs(-56.777));
		System.out.println(Math.floor(3.14159));
		System.out.println(Math.ceil(3.14159));
		System.out.println(Math.max(3.14159, 4.111));
		System.out.println(Math.min(3.14159, 4.111));
		System.out.println(Math.pow(2, 5));   //2^5
		System.out.println(Math.sqrt(16));    //16 = 4^2
	}

}
